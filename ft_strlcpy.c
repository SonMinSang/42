#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	index;
	size_t	src_size;

	index = 0;
	src_size = 0;
	while (src[src_size])
		src_size++;
	while (index < src_size && index + 1 < size)
	{
		dst[index] = src[index];
		index++;
	}
	if (size > 0)
		dst[index] = '\0';
	return (src_size);
}
